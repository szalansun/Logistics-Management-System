/*==============================================================*/
/* Table: PROVINCE                                              */
/*==============================================================*/
CREATE TABLE PROVINCE
(
   PROVINCEID           BIGINT UNSIGNED NOT NULL AUTO_INCREMENT,
   NAME                 VARCHAR(20),
   PRIMARY KEY (PROVINCEID)
