/*==============================================================*/
/* Table: RECEIPT                                               */
/*==============================================================*/
CREATE TABLE RECEIPT
(
   RECEIPTID            BIGINT UNSIGNED NOT NULL AUTO_INCREMENT,
   CARNO                VARCHAR(20),
   DELIVERYSPOTID       INT,
   EMP_EMPID            INT,
   FROMNAME             VARCHAR(20),
   TONAME               VARCHAR(20),
   CREATETIME           VARCHAR(20),
   REMARK               VARCHAR(500),
   FLAG                 INT,
   EMPID                INT,
   RECEIPTNO            VARCHAR(30),
   RTYPE                VARCHAR(20),
   PRIMARY KEY (RECEIPTID)
);

/*==============================================================*/
/* Table: RECEIPTDETAILE                                        */
