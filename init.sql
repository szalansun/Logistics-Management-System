/*==============================================================*/
/* Table: CAR                                                   */
/*==============================================================*/
CREATE TABLE CAR
(
   CARID                BIGINT UNSIGNED NOT NULL AUTO_INCREMENT,
   CARNO                VARCHAR(20),
   WORKNO               VARCHAR(20),
   CARTYPEID            INT,
   CARSIZE              INT,
   TONNAGE              INT,
   BUYTIME              DATE,
   LEAVETIME            DATE,
   CARSTATEID           INT,
   LINEID               INT,
   EMPID                INT,
   REMARK               VARCHAR(500),
   PRIMARY KEY (CARID)
);

/*==============================================================*/
/* Table: CARSTATE                                              */
/*==============================================================*/
CREATE TABLE CARSTATE
(
   CARSTATEID           BIGINT UNSIGNED NOT NULL AUTO_INCREMENT,
   NAME                 VARCHAR(20),
   PRIMARY KEY (CARSTATEID)
);

/*==============================================================*/
/* Table: CARTYPE                                               */
/*==============================================================*/
CREATE TABLE CARTYPE
(
   CARTYPEID            BIGINT UNSIGNED NOT NULL AUTO_INCREMENT,
   NAME                 VARCHAR(20),
   PRIMARY KEY (CARTYPEID)
);

/*==============================================================*/
/* Table: CITY                                                  */
/*==============================================================*/
CREATE TABLE CITY
(
   CITYID               BIGINT UNSIGNED NOT NULL AUTO_INCREMENT,
   NAME                 VARCHAR(20),
   ZIPCODE				VARCHAR(10),
   PROVINCEID           INT,
   PRIMARY KEY (CITYID)
);

/*==============================================================*/
/* Table: DELIVERYSPOT                                          */
/*==============================================================*/
CREATE TABLE DELIVERYSPOT
(
   DELIVERYSPOTID       BIGINT UNSIGNED NOT NULL AUTO_INCREMENT,
   DELIVERYSPOTNO       VARCHAR(20),
   NAME                 VARCHAR(20),
   PROVINCEID           INT,
   CITYID               INT,
   EMPID                INT,
   TEL                  VARCHAR(20),
   STARTSCOPE           DECIMAL(9,2),
   STARTPRICE           DECIMAL(9,2),
   SECONDPRICE          DECIMAL(9,2),
   STARTVOLUMESCOPE     DECIMAL(9,2),
   STARTVOLUMEPRICE     DECIMAL(9,2),
   SECONDVOLUMEPRICE    DECIMAL(9,2),
   FLAG					INT,
   PRIMARY KEY (DELIVERYSPOTID)
);

/*==============================================================*/
/* Table: EMPLOYEE                                              */
/*==============================================================*/
CREATE TABLE EMPLOYEE
(
   EMPID                BIGINT UNSIGNED NOT NULL AUTO_INCREMENT,
   EMPNO                VARCHAR(20),
   NAME                 VARCHAR(20),
   GENDER               INT,
   BIRTHDAY             DATE,
   ADDRESS              VARCHAR(100),
   TEL                  VARCHAR(20),
   STARTWORDTIME        DATE,
   SALARY               DECIMAL(9,2),
   EMAIL                VARCHAR(50),
   CARDID               VARCHAR(20),
   POSITIONID           INT,
   DELIVERYSPOTID       INT,
   FLAG                 INT,
   PRIMARY KEY (EMPID)
);

/*==============================================================*/
/* Table: LINE                                                  */
/*==============================================================*/
CREATE TABLE LINE
(
   LINEID               BIGINT UNSIGNED NOT NULL AUTO_INCREMENT,
   LINENO               INT,
   LINENAME             VARCHAR(20),
   LENGTH               INT,
   PRIMARY KEY (LINEID)
);

/*==============================================================*/
/* Table: LINEDETAILE                                           */
/*==============================================================*/
CREATE TABLE LINEDETAILE
(
   LINEDETAILEID        BIGINT UNSIGNED NOT NULL AUTO_INCREMENT,
   LINEID               INT,
   DELIVERYSPOTID       INT,
   LINEINDEX            INT,
   PRIMARY KEY (LINEDETAILEID)
);

/*==============================================================*/
/* Table: ODER                                                  */
/*==============================================================*/
CREATE TABLE ODER
(
   ORDERID              BIGINT UNSIGNED NOT NULL AUTO_INCREMENT,
   ORDERNO              VARCHAR(50),
   SENDNAME             VARCHAR(20),
   SENDADDRESS          VARCHAR(100),
   SENDTEL              VARCHAR(20),
   SENDMOBILE           VARCHAR(20),
   SENDPOSTCODE         VARCHAR(20),
   RECEIVENAME          VARCHAR(20),
   RECEIVEADDRESS       VARCHAR(100),
   RECEIVETEL           VARCHAR(20),
   RECEIVEMOBILE        VARCHAR(20),
   RECEIVEPOSTCODE      VARCHAR(20),
   ORDERSTATEID         INT,
   TOTALPRICE           DECIMAL(9,2),
   INSUREPRICE          DECIMAL(9,2),
   GOODSPRICE           DECIMAL(9,2),
   VOLUME               DECIMAL(9,2),
   WEIGHT               DECIMAL(9,2),
   REMARK               VARCHAR(500),
   CREATETIME           DATE,
   DELIVERYSPOTID       INT,
   PRIMARY KEY (ORDERID)
);

/*==============================================================*/
/* Table: ORDERSTATE                                            */
/*==============================================================*/
CREATE TABLE ORDERSTATE
(
   ORDERSTATEID         BIGINT UNSIGNED NOT NULL AUTO_INCREMENT,
   `NAME`                 VARCHAR(20),
   PRIMARY KEY (ORDERSTATEID)
